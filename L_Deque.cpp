ll go(vi &v, int i, int j, bool ch, vector<vector<ll>>&dp)
{
    if (i > j)
    {
        return 0;
    }
    if(dp[i][j]!=-1)
    return dp[i][j];
    ll x = v[i]-go(v, i + 1, j, !ch,dp);
    ll y = v[j]-go(v, i, j - 1, !ch,dp);
    return dp[i][j]=max(x,y);
}
